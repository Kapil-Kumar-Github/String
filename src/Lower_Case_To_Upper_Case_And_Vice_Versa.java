public class Lower_Case_To_Upper_Case_And_Vice_Versa {


    public static void main(String [] args){


        String lowerCase = "kapil";
        System.out.println("Lower Case string be " + lowerCase) ;

        System.out.println("Upper Case of above String be "+lowerCase.toUpperCase()) ;

        String upperCase = "GULSHAN" ;

        System.out.println("Upper Case string be " + upperCase);
        System.out.println("Lower Case of above string be " + upperCase.toLowerCase()) ;
    }
}
